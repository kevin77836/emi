                            (一) 教材或教案研發費<br>
                            1.開設國際合作課程之本校教師，應致力於教學方法、教材設計及運用等面向的研發與創新，每開設1學分課程可獲補助2萬元為上限， 最高獲補助6萬元為上限。如有多名教師共同開課，則依人數比例支領。教師申請國際合作課程補助與校內其他相關課程補助，擇一支領，不得重複申請。 <br>
