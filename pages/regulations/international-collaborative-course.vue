                            （三）國際師資合開之課程，本校授課教師需同時在場。<br><br>

                            四、採英語授課為原則，得視學生理解課程之語言需要，採雙語（例如華語、英語或其他二種以上外語）方式授課。<br><br>

                            五、課程學分、學習時數核發及課程評量等逕依本校規定辦理。 <br><br>
