                            4. 前開補助依「行政院各機關聘請國外顧問、專家及學者來臺工作期間支付費用最高標準表」、「講座鐘點費支給表」暨相關補助規範辦理，申請時需衡酌國外專家學者國際聲譽、學術地位、 課程內容及延聘難易程度等相關條件後併同佐證，簽請同意後辦理。<br><br>

